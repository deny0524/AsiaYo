#!/bin/bash
# --------------------------------------------------
# 題目一：找出 words.txt 中重複次數最多的單字（忽略大小寫）
#
# 思路：
#   1. tr 將所有非字母字元替換為換行，使每行一個單字
#   2. tr 統一轉小寫，讓 The/the 視為同一單字
#   3. grep -v '^$' 過濾空行
#   4. sort | uniq -c 計算每個單字出現次數
#   5. sort -rn 依次數降序排列，取第一筆即為最多者
# --------------------------------------------------

tr -cs 'A-Za-z' '\n' < "${1:-words.txt}" \
  | tr 'A-Z' 'a-z' \
  | grep -v '^$' \
  | sort \
  | uniq -c \
  | sort -rn \
  | head -1 \
  | awk '{print $1, $2}'
